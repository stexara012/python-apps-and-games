# ==========================
# Osnovna ideja:
# - Zmija = niz segmenata (x,y)
# - Glava pokazuje pravac kretanja (dx, dy)
#   (-TILE_SIZE, 0) = levo
#   (TILE_SIZE, 0)  = desno
#   (0, -TILE_SIZE) = gore
#   (0, TILE_SIZE)  = dole
# - Rep se okreće prema sledećem segmentu
# - Telo može biti horizontalno ili vertikalno
# - Ugaoni segmenti crtaju se kad zmija menja pravac
# - Wave efekat: offset = A * sin(t + i*k), gde je i = indeks segmenta

# Ugaoni segmenti:
# - gledamo prethodni i sledeći segment:
#   vektor_prev = (x_prev - x_curr, y_prev - y_curr)
#   vektor_next = (x_next - x_curr, y_next - y_curr)
# - na osnovu kombinacije biramo ugaoni ASCII sprite (UL, UR, DL, DR)
# - rotacija jednog sprite-a (npr. UR) rešava clockwise i counter-clockwise

# Mala školica matematike:
# dx/dt = promena x-koordinate po vremenu
# dy/dt = promena y-koordinate po vremenu
# Tabela:
# dx/dt = 0  -> vertikalno telo -> body_sprite_up/down
# dy/dt = 0  -> horizontalno telo -> body_sprite_left/right

# Vizuelna shema:
#       prev
#        •
#        |
#        | dy/dt ≠ 0
#        |
#      segment (x,y)
#        |
#        |
#        •
#       next
# - Ako segment menja pravac, crtamo ugaoni sprite:
#       • prev
#        \
#         \
#          ◦ segment
#         /
#        /
#       • next
# ==========================
# ==========================
# ASCII Snake - pravci i uglovi (CW & CCW)
# ==========================
# Pravci glave:
#    ↑    (0,-TILE_SIZE) - head_sprite_up
#    ↓    (0,TILE_SIZE)  - head_sprite_down
#    ←    (-TILE_SIZE,0) - head_sprite_left
#    →    (TILE_SIZE,0)  - head_sprite_right
#
# Ugaoni segmenti:
# ◦ = trenutni segment
# • = prethodni / sledeći segment
#
#       UL (Counter-Clockwise)
#       • prev
#       |
#       ◦ segment
#       ←
#   next •
#
#       UR (Clockwise)
#       • prev
#       |
#       ◦ segment
#       →
#   next •
#
#       DL (Clockwise)
#   next •
#       ◦ segment
#       |
#       • prev
#       ↓
#
#       DR (Counter-Clockwise)
#   next •
#       ◦ segment
#       |
#       • prev
#       ↑
#
# Horizontalno telo: dx/dt ≠ 0, dy/dt = 0 -> body_sprite_left/right
# Vertikalno telo: dx/dt = 0, dy/dt ≠ 0 -> body_sprite_up/down
# Wave efekat: offset = A * sin(t + i*k)
